async def update_plate_table() -> str:
    """更新完成表"""
    try:
        version = list(_ for _ in DX_VERSION.keys())[1:]
        # version.append("霸")
        # version.append("舞")
        id_bg = Image.new("RGBA", (100, 20), (124, 129, 255, 255))
        atime = 0
        for _v in version:
            _otime = time.time()
            if _v in PLATE_CN:
                _v = PLATE_CN[_v]
            ver, _ver = VERSION_MAP.get(_v, ([DX_VERSION.get(_v)], _v))
            
            music_id_list = mai.total_plate_id_list[_ver]
            music = mai.total_list.by_id_list(music_id_list)
            ralv: dict[str, list[Song]] = {lv: [] for lv in reversed(LEVEL_LIST)}

            for m in music:
                ralv[m.difficulties[3].level].append(m)

            lines = 0
            interval = 0
            for _ in ralv:
                musicnum = len(ralv[_])
                if musicnum == 0:
                    continue
                interval += 1
                remainder = musicnum % 10
                lines += (musicnum // 10) + (1 if remainder else 0)
            
            linesheight = 115 * lines + (interval - 1) * 15
            """
            `linesheight`: 各等级曲绘和间隔总和高度
            
                - `115` 为曲绘高度 `100` + 间隔 `15`
                - `lines` 为行数
                - `interval` 为各等级间隔行数
                - `(interval - 1) * 15` 为各等级间隔高度，各等级之间间隔为 `30`，所以只加 `15`
            """
            width, height = 1400, 150 + linesheight + 360
            """
            `150` 为底部图片 `design` 高度 + 上下间隔高度
            `linesheight` 为各等级曲绘和间隔总和高度
            `360` 为顶部图片 `` 高度 + 上下间隔高度
            """

            im = tricolor_gradient_prism_plus(width, height)
            
            im.alpha_composite(ScoreBaseImage._aurora_bg)
            im.alpha_composite(ScoreBaseImage._shines_bg, (34, 0))
            im.alpha_composite(ScoreBaseImage._cloud_bg, (319, height - 643))
            im.alpha_composite(ScoreBaseImage._rainbow_bottom_bg, (100, height - 343))
            for h in range((height // 358) + 1):
                im.alpha_composite(ScoreBaseImage._pattern_bg, (0, (358 + 7) * h))
            
            dr = ImageDraw.Draw(im)
            ts = DrawText(dr, TBFONT)
            sy = DrawText(dr, SIYUAN)
            im.alpha_composite(ScoreBaseImage._design_prism_bg, (200, height - 113))
            sy.draw(
                700, height - 70, 22, 
                f"Designed by Yuri-YuzuChaN & BlueDeer233. Generated by {maiconfig.botname} BOT", 
                ScoreBaseImage._default_text_color, "mm"
            )
            y = 245
            for r in ralv:
                if _v in ["霸", "舞"]:
                    ralv[r].sort(key=lambda x: x.difficulties[-1].level_value, reverse=True)
                else:
                    ralv[r].sort(key=lambda x: x.difficulties[3].level_value, reverse=True)
                if ralv[r]:
                    y += 15
                    im.alpha_composite(ScoreBaseImage._level_bg, (65, y + 115))
                    ts.draw(113, y + 164, 35, r, anchor="mm")
                x = 200
                for num, music in enumerate(ralv[r]):
                    if num % 10 == 0:
                        x = 200
                        y += 115
                    else:
                        x += 115
                    cover = song_chart(music.song_id)
                    im.alpha_composite(Image.open(cover).resize((100, 100)), (x, y))
                    im.alpha_composite(id_bg, (x, y + 80))
                    ts.draw(x + 50, y + 88, 20, music.song_id, anchor="mm")

            by = BytesIO()
            im.save(by, "PNG")
            async with aiofiles.open(plate_dir / f"{_v}.png", "wb") as f:
                await f.write(by.getbuffer())
            _ntime = int(time.time() - _otime)
            atime += _ntime
            log.info(f"{_v}代牌子更新完成，耗时：{_ntime}s")
        result = f"完成表更新完成，耗时：{atime}s"
        log.info(result)
    except Exception as e:
        log.error(traceback.format_exc())
        result = f"完成表更新失败，Error: {e}"
    return result